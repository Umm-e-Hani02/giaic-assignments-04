# This program prints even numbers from 0 to 38 by multiplying each number in range(20) by 2

def main():
    # Loop through numbers 0 to 19
    for i in range(20):
        # Multiply each number by 2 to get even numbers and print them
        print(i*2)

# Run the main function only if this file is run directly
if __name__ == '__main__':
    main()