def main():
    # loop through the numbers 10 to 20
    for i in range(10,20):
        # if i is even, print that it is even
        if i % 2 == 0:
            print(f"{i} is even")
        # if i is odd, print that it is odd
        else:
            print(f"{i} is odd")

if __name__ == "__main__":
    main()
